// Aritmético
// +  adição / Concatenação
// -  Subtração
// *  Multiplicação
// /  Divisão
// ** Potenciação
// &  Resto da divisão

// Precedência
// ()
// **
// * , / , %
// + , - 

const num1 = 5;
const num2 = 2;
const num3 = 10;
const resultado = (num1 + num2) * num3;
console.log(resultado);

// Incremento
// ++
let contador = 1;
contador++;  // ou ++contador;
contador++;  // ou ++contador;
console.log(contador);

// Decremento
// --
let contar = 10;
contar--;  // ou --contar;
contar--;  // ou --contar;
console.log(contar);

// Atribuição
// +=
// -=
// *=
// /=
// **=
let atribuicao = 2;
atribuicao *= 2;
atribuicao *= 2;
console.log(atribuicao);

